import socket


def is_port_open(host:str, port:int) -> bool:
    s:socket.socket | None = None
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        s.settimeout(1)
        s.connect((host, port))
        return True
    except Exception:
        return False
    finally:
        if s:
            s.close()
